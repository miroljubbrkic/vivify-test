class Namirnica:
    def __init__(self,naziv):
        self.naziv = naziv
        self.cena = 0

    def get_naziv(self):
        return self.naziv

    def set_naziv(self,value):
        self.naziv = value

    def get_cena(self):
        return self.cena

    def set_cena(self,value):
        self.cena = value
